import smtplib
from email.mime.multipart import MIMEMultipart
from email.mime.text import MIMEText
from email.mime.image import MIMEImage

def Mail_send(mailid, image_paths):
    # Set up the SMTP server
    server = smtplib.SMTP('smtp.gmail.com', 587)
    server.starttls()
    server.login('www.vishalgp26@gmail.com', 'deax kngr xyxa qlwz')

    # Create a multipart message
    msg = MIMEMultipart()

    # Set email sender and receiver
    msg['From'] = 'www.vishalgp26@gmail.com'
    msg['To'] = mailid  # Ensure mailid is a string, not a tuple

    # Set the subject
    msg['Subject'] = "Demo Flat Photos"

    # Attach the message body
    body = 'This mail is autogenerated by Vishal_Pawar'
    msg.attach(MIMEText(body, 'plain'))

    # Attach the image
    for image_path in image_paths:
        with open(image_path, 'rb') as img_file:
            img = MIMEImage(img_file.read())
            img.add_header('Content-Disposition', 'attachment', filename=image_path)
            msg.attach(img)


    server.send_message(msg)
    print('Mail sent Successfully')

    # Terminate the SMTP session
    server.quit()